//Base skeleton Java class For student, parent of Mentor and Mentee Classes
public class Student {
    private String name;
    private String email;

    public Student()
    {
        name = " ";
        email = " ";
    }

    public Student (String name, String email)
    {
        this.name = name;
        this.email = email;
    }
}
